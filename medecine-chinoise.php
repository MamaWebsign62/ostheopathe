                 <p>Dans le courant du XIX° siècle, grâce aux jésuites revenant de Chine, la Médecine Traditionnelle Chinoise est apparue timidement en Occident, et en particulier en France. Même si elle s'est rapidement répandue dans le monde, ce n'est pourtant que dans les années 1970 que cette science a pris chez nous son véritable essor, tant au niveau de sa pratique que dans l'organisation de son enseignement. De nos jours, elle est donc encore relativement peu connue. Elle est nommée par les uns "médecine parallèle", par d'autres "douce", "alternative" ou encore "complémentaire", sans savoir d'ailleurs très bien ce que recouvrent ces expressions.</p>
                 <p>Ce site a pour but de faire découvrir (ou redécouvrir) à nos contemporains ce type de médecine non seulement très riche culturellement et scientifiquement, mais aussi très efficace et éthiquement juste. Indéniablement, la Médecine Traditionnelle Chinoise apporte aujourd'hui à notre société une réponse crédible quant aux grands problèmes de santé.</p>
