            <h2><i class="fa fa-circle" aria-hidden="true"></i>A quoi correspondent les points d’acupuncture ?</h2>
            <div class="paragraph-box2 medecine-chinoise">
              <p>Il s’agit d’endroits très précis (moins d’un millimètre carré) situés sur les méridiens dans leur partie superficielle.</p>
              <p>Il en existe plus de 400, mais un praticien n’en utilise habituellement que 150 environ et une centaine dans sa pratique courante.</p>
              <p>Plusieurs études scientifiques ont montré qu’à cet endroit précis de la peau, la différence de potentiel entre la partie superficielle et la partie profonde est significativement différente d’un autre endroit pris au hasard. Ceci n’explique évidemment rien, mais montre tout de même qu’un phénomène très particulier existe.</p>
            </div>
          </div>

