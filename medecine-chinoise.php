               <div class="paragraph-box medecine-chinoise">
                 <p>Traditionnellement, les aiguilles ne sont laissées en place que quelques secondes pour obtenir une dispersion ; si le praticien veut obtenir un effet de tonification, les aiguilles resteront posées quelques minutes.</p>
                 <p>Si une aiguille reste placée pendant de nombreuses minutes, se produit alors un effet d’«harmonisation» (technique utilisée par exemple lorsque deux fonctions ne sont pas en harmonie entre elles).</p>
