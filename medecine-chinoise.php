          <div class="titre3">
            <h2><i class="fa fa-circle" aria-hidden="true"></i>Combien d'aiguilles sont-elles posées lors d'une séance ?</h2>
            <div class="paragraph-box2 medecine-chinoise">
              <p>Rappelons que la régularisation énergétique fait travailler l’organisme dans un certain sens, afin d’arriver à une harmonisation. De ce fait, on comprend bien qu’il n’est pas possible de poser de trop nombreuses aiguilles dans la même séance, pour ne pas faire travailler l’organisme dans trop de directions simultanément.</p>
              <p>Habituellement, en Occident, le praticien ne pose pas plus de 7 à 8 aiguilles par séance, pour ne pas trop fatiguer le patient et ainsi optimiser la réponse de l’organisme à la stimulation acupuncturale.</p>
              <p>Le nombre d’aiguilles posées dépend aussi d’autres facteurs laissés à l’appréciation du praticien : âge du malade, chronicité de la maladie, saison, hygiène de vie, etc.</p>
            </div>
          </div>
