        <div class="tribox-femme">
           <img src="./img/femme-enceinte2.jpg" width="298" height="199" alt="photo-femme-enceinte">
           <div class="titre2">
               <h2><i class="fa fa-circle" aria-hidden="true"></i>Quelles modifications subit votre corps pendant la grossesse ?</h2>
               <div class="paragraph-box">
                 <p>Si vous attendez un bébé, notre cabinet d'ostéopathie dispense des consultations adaptées à l'état de grossesse.</p>
                 <p>Le fait d'attendre un bébé cause des transformations importantes dans votre corps. Des changements hormonaux vont causer des modifications au niveau des tissus du corps. Les tissus deviennent plus souples au moment de la grossesse pour faciliter le passage du futur bébé au niveau du bassin.</p>
                 <p>Le poids et la forme de votre ventre vont causer un changement de posture. Le centre de gravité de votre corps va bouger et se positionner plus en avant. La grossesse va également provoquer une hyper-pression dans la région de la cavité abdominale ; les viscères devront alors trouver une nouvelle place.</p>
                 <p>Toutes ces modifications vont provoquer de nombreux dysfonctionnements entraînant des douleurs que l'ostéopathe saura calmer.</p>
               </div>
           </div>
         </div>

        <div class="tribox-femme tribox2 ">
          <div class="titre3">
            <h2><i class="fa fa-circle" aria-hidden="true"></i>Pour quelles raisons est-il intéressant de consulter un ostéopathe pendant votre grossesse ?</h2>
            <p>Par conséquent, une femme enceinte peut faire appel à un ostéopathe pour de nombreuses raisons : mécaniques, mais aussi digestives, circulatoires, etc. Les douleurs mécaniques, comme les lombalgies, sont dues à tous les changements qui apparaissent pendant la grossesse. La prise de poids et les forces antérieures qui viennent tirer sur les lombaires (insertions des fascias sur les vertèbres) accentuent la cambrure lombaire. La femme enceinte souffrira aussi fréquemment de sciatalgie ou sciatique. La sciatique est due principalement à un pincement du nerf sciatique. Il peut être comprimé lors de son passage dans le muscle pyramidal. Les douleurs du coccyx et du bassin sont aussi assez fréquentes.</p>
            <p>Une femme enceinte pourra aussi consulter un ostéopathe si elle est sujette à des troubles digestifs comme des constipations, des ballonnements, des diarrhées, des reflux gastro-oesophagiens ou des brûlures d'estomac.</p>
            <p>Vous pouvez aussi consulter un ostéopathe pendant votre grossesse pour des troubles circulatoires, c'est à dire des migraines, des oedèmes au niveau des membres inférieurs ou des sensations de lourdeur au niveau des jambes.</p>
            <p>Même en l'absence de troubles déclarés, une femme enceinte peut consulter un ostéopathe à titre préventif. Une telle consultation permet d'éviter la survenance des troubles évoqués ci-dessus et de bénéficier de séances de préparation à l'accouchement.</p>
          </div>

