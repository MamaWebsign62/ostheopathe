              <h2><i class="fa fa-circle" aria-hidden="true"></i>Comment se déroule la consultation ?</h2>
              <div>
                <p>L'ostéopathe va dans un premier temps observer la patiente pour étudier comment elle se tient et examiner ses courbures. Il va ensuite regarder spécialement les articulations au niveau du bassin et identifier des problèmes ou blocages potentiels. Il va enfin vérifier si les mobilités du thorax, de l'abdomen et des structures crâniennes sont bonnes.</p>
                <p>La totalité des techniques auxquelles a recours l'ostéopathie sont ajustées à chaque patiente et à chaque contexte de consultation. Elles sont bien entendu non invasives pour le bébé. On peut consulter un ostéopathe dès le troisième mois de grossesse y compris en préventif. En effet, en général les douleurs apparaissent à partir du 6ème mois de grossesse. Une à deux séances sont alors nécessaires pour traiter la cause des douleurs, puis une à deux consultations permettent de préparer l'accouchement.</p>
                <p>N'hésitez pas à nous contacter pour davantage d'informations ou bien prendre un rendez-vous.</p>
