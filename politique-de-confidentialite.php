  <h1>Politique de confidentialité</h1>
  <p>Une politique de confidentialité est un contrat qui décrit comment une société retient, traite, publie et efface les données transmises par ses clients.</p>

  <h2>Qui sommes-nous ?</h2>

  <p>L’adresse de notre site Web est : www.kipets.fr.</p>

